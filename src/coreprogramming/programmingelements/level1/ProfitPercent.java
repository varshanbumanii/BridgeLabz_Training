package coreprogramming.programmingelements.level1;

public class ProfitPercent {
	public static void main(String[] args) {
		double profit = 191 - 129;
		double percent =( profit/129 )*100;
		System.out.println("Cost Price : Rs. 129 and Selling Price : Rs. 191");
		System.out.println("Profit is : Rs. "+profit+" and Profit Percentage : "+percent+"%");
		
	}
}
